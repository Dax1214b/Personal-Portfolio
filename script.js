// Email validation function
function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

// Notification system
function showNotification(message, type = 'info') {
    // Remove existing notifications
    const existingNotification = document.querySelector('.notification');
    if (existingNotification) {
        existingNotification.remove();
    }
    
    // Create notification element
    const notification = document.createElement('div');
    notification.className = `notification notification-${type}`;
    notification.innerHTML = `
        <div class="notification-content">
            <span class="notification-message">${message}</span>
            <button class="notification-close">&times;</button>
        </div>
    `;
    
    // Add styles
    notification.style.cssText = `
        position: fixed;
        top: 20px;
        right: 20px;
        background: ${type === 'success' ? '#10b981' : type === 'error' ? '#ef4444' : '#3b82f6'};
        color: white;
        padding: 1rem 1.5rem;
        border-radius: 8px;
        box-shadow: 0 10px 25px rgba(0, 0, 0, 0.1);
        z-index: 10000;
        transform: translateX(100%);
        transition: transform 0.3s ease;
        max-width: 400px;
    `;
    
    // Add to page
    document.body.appendChild(notification);
    
    // Animate in
    setTimeout(() => {
        notification.style.transform = 'translateX(0)';
    }, 100);
    
    // Close button functionality
    const closeBtn = notification.querySelector('.notification-close');
    closeBtn.addEventListener('click', () => {
        notification.style.transform = 'translateX(100%)';
        setTimeout(() => notification.remove(), 300);
    });
    
    // Auto remove after 5 seconds
    setTimeout(() => {
        if (notification.parentNode) {
            notification.style.transform = 'translateX(100%)';
            setTimeout(() => notification.remove(), 300);
        }
    }, 5000);
}

// Dynamic typing effect for hero subtitle
function typeWriter(element, text, speed = 100, callback) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        } else if (callback) {
            setTimeout(callback, 2000); // Wait 2 seconds before erasing
        }
    }
    
    type();
}

function eraseText(element, speed = 50, callback) {
    let text = element.innerHTML;
    
    function erase() {
        if (text.length > 0) {
            text = text.slice(0, -1);
            element.innerHTML = text;
            setTimeout(erase, speed);
        } else if (callback) {
            setTimeout(callback, 500); // Wait 0.5 seconds before typing next
        }
    }
    
    erase();
}

// Initialize dynamic typing animation
document.addEventListener('DOMContentLoaded', () => {
    const typingElement = document.querySelector('.typing-text');
    if (typingElement) {
        const roles = [
            'Machine Learning Engineer',
            'Data Scientist',
            'AI Researcher',
            'Python Developer',
            'Deep Learning Specialist'
        ];
        
        let currentRoleIndex = 0;
        
        function cycleRoles() {
            const currentRole = roles[currentRoleIndex];
            
            typeWriter(typingElement, currentRole, 100, () => {
                eraseText(typingElement, 50, () => {
                    currentRoleIndex = (currentRoleIndex + 1) % roles.length;
                    cycleRoles();
                });
            });
        }
        
        // Start the typing cycle
        setTimeout(cycleRoles, 1000);
    }
});

